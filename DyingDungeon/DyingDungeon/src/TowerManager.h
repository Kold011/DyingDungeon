#pragma once
#include "Component.h"
#include "BattleInstance.h"
#include "Buffs.h"
#include "UICanvas.h"
#include "Text2D.h"

enum TOWER_STATE
{
	NOT_IN_BATTLE,
	IN_BATTLE,
	IN_REWARDS
};

class TowerManager : public Odyssey::Component
{
	CLASS_DECLARATION(TowerManager);
	
public: // Constructors
	TowerManager(GameObjectList _playerTeam, GameObjectList _enemyTeam, int _numberOfBattles);
	virtual ~TowerManager();

	Odyssey::UICanvas* UI;
	Odyssey::UICanvas* Rewards;
	std::vector<Odyssey::Text2D*> TurnOrderNumbers;

public: // Functions

	// Necessary
	virtual void initialize();
	virtual void update(double deltaTime);
	
	//Getters
	int GetTowerState() { return mTowerState; }

	GameObjectList GetEnemyTeam() { return mEnemyTeam; } // This will allow the battle instance to get the enemy team genreated by the Overlord

	std::shared_ptr<float> GetStatitics() { return mStatitics; } // This will return the statics that the Overlord will be using to construct a new enemy team

	int GetCurrentLevel() { return mCurrentLevel; }

	BattleInstance* GetBattleInstance() { return mCurrentBattle; }

	//Setters
	void SetTowerState(TOWER_STATE _newTowerState) { mTowerState = _newTowerState; }

	void SetEnemyTeam(GameObjectList _newEnemyTeam) { mEnemyTeam = _newEnemyTeam; } // This will set the enemy team to the new enemy team that is generated by the Overlord

	void SetStatitics(std::shared_ptr<float> _newStatitics) { mStatitics = _newStatitics; } // This will set a list of new statitics to the mStatitics varibale that will be used in the Overlord

	void SetCurrentLevel(int _newLevel) { mCurrentLevel = _newLevel; }

private: // Varibales

	//Battle Instance
	BattleInstance* mCurrentBattle;

	// Vectors
	GameObjectList mAllCharacters; // allCharacters will hold all of the game objects that are in the current battle
	GameObjectList mPlayerTeam; // playerTeam will hold the gameObjects associated with the Player's team
	GameObjectList mEnemyTeam; // enemyTeam will hold the gameObjects associated with the Overlord's team

	// Queues

	// GameObjects

	// Ints
	int mCurrentLevel;
	int mNumberOfLevels;

	// Floats
	std::shared_ptr<float> mStatitics;

	// Bools

	// Tower State
	TOWER_STATE mTowerState;

private: // Functions
	void CreateBattleInstance();
	void DestroyBattleInstance();
	void GoToMainMenu();
};

